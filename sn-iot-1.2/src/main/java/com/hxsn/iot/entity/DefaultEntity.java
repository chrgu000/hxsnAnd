package com.hxsn.iot.entity;

/**
 * 典型的实体类，可以代表多种实体类
 * Created by jiely on 2017/4/19.
 */
public class DefaultEntity {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
